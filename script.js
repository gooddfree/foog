// ملء القائمة
surahs.forEach((name, index) => {
  const option = document.createElement('option');
  option.value = String(index + 1).padStart(3, '0');
  option.textContent = `${index + 1}. ${name}`;
  surahSelect.appendChild(option);
});

// التشغيل
function playSurah() {
  const surahNum = surahSelect.value;
  const audioUrl = `https://server8.mp3quran.net/minsh/${surahNum}.mp3`;
  player.src = audioUrl;
  player.play();
}
